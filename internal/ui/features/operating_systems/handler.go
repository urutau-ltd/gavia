// Create handles POST /os and persists a new operating system.
// After successful creation it returns both refreshed editor and list fragments for HTMX.
func (h *Handler) Create(w http.ResponseWriter, r *http.Request) {
	if err := r.ParseForm(); err != nil {
		http.Error(w, "invalid form", http.StatusBadRequest)
		return
	}

	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list operating systems", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	name := strings.TrimSpace(r.Form.Get("name"))
	notes := strings.TrimSpace(r.Form.Get("notes"))

	data.EditorMode = "new"
	data.FormAction = "/os"
	data.FormSubmit = "Create operating system"
	data.OperatingSystem = &operatingsystem.OperatingSystem{
		Name:  name,
		Notes: ui.OptionalString(notes),
	}

	if name == "" {
		data.ErrorHTML = bannerHTML("bad", "Name is required.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusBadRequest)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	if err := h.osRepo.Create(r.Context(), data.OperatingSystem); err != nil {
		h.logger.Error("Failed to create operating system", "err", err)
		status := http.StatusInternalServerError
		msg := "Unable to create operating system."
		if strings.Contains(strings.ToLower(err.Error()), "unique") {
			status = http.StatusConflict
			msg = "An operating system with that name already exists."
		}
		data.ErrorHTML = bannerHTML("bad", msg)
		ui.WriteHTMLHeader(w)
		w.WriteHeader(status)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	updatedOperatingSystems, listErr := h.osRepo.GetAll(r.Context(), data.SearchTerm, data.Limit)
	if listErr != nil {
		h.logger.Error("Failed to refresh operating system list", "err", listErr)
	} else {
		data.OperatingSystems = updatedOperatingSystems
	}

	data.EditorMode = "detail"
	data.NoticeHTML = bannerHTML("ok", "Operating system created successfully.")

	ui.WriteHTMLHeader(w)
	if h.isEditorRequest(r) {
		h.renderTemplate(w, "os-editor-response", data)
		return
	}

	h.renderTemplate(w, "base", data)
}

// Update handles POST /os/{id}/edit and persists operating system changes.
// It validates existence first to distinguish not-found from write failures.
func (h *Handler) Update(w http.ResponseWriter, r *http.Request) {
	if err := r.ParseForm(); err != nil {
		http.Error(w, "invalid form", http.StatusBadRequest)
		return
	}

	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list operating systems", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	id := r.PathValue("id")
	name := strings.TrimSpace(r.Form.Get("name"))
	notes := strings.TrimSpace(r.Form.Get("notes"))

	data.EditorMode = "edit"
	data.FormAction = fmt.Sprintf("/os/%s/edit", id)
	data.FormSubmit = "Update operating system"
	data.OperatingSystem = &operatingsystem.OperatingSystem{
		Id:    id,
		Name:  name,
		Notes: ui.OptionalString(notes),
	}

	if name == "" {
		data.ErrorHTML = bannerHTML("bad", "Name is required.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusBadRequest)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	current, findErr := h.osRepo.GetByID(r.Context(), id)
	if findErr != nil {
		h.logger.Error("Failed to load operating system for update", "id", id, "err", findErr)
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("bad", "Unable to validate operating system before update.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusBadRequest)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	if current == nil {
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("warn", "Operating system not found.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusNotFound)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	if err := h.osRepo.Update(r.Context(), data.OperatingSystem); err != nil {
		h.logger.Error("Failed to update operating system", "id", id, "err", err)
		status := http.StatusInternalServerError
		msg := "Unable to update operating system."
		if strings.Contains(strings.ToLower(err.Error()), "unique") {
			status = http.StatusConflict
			msg = "An operating system with that name already exists."
		}
		data.ErrorHTML = bannerHTML("bad", msg)
		ui.WriteHTMLHeader(w)
		w.WriteHeader(status)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	refreshed, getErr := h.osRepo.GetByID(r.Context(), id)
	if getErr != nil {
		h.logger.Error("Failed to reload operating system after update", "id", id, "err", getErr)
	} else if refreshed != nil {
		data.OperatingSystem = refreshed
	}

	updatedOperatingSystems, listErr := h.osRepo.GetAll(r.Context(), data.SearchTerm, data.Limit)
	if listErr != nil {
		h.logger.Error("Failed to refresh operating system list", "err", listErr)
	} else {
		data.OperatingSystems = updatedOperatingSystems
	}

	data.EditorMode = "detail"
	data.NoticeHTML = bannerHTML("ok", "Operating system updated successfully.")

	ui.WriteHTMLHeader(w)
	if h.isEditorRequest(r) {
		h.renderTemplate(w, "os-editor-response", data)
		return
	}

	h.renderTemplate(w, "base", data)
}

