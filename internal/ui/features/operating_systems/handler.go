type Handler struct {
	logger *slog.Logger
	tmpl   *template.Template
	osRepo *operatingsystem.OperatingSystemRepository
}

// pageData is the template contract for operating systems screens.
// Keeping view state in one struct makes it explicit which fields frive
// the list/editor partials.
type pageData struct {
	// this data is generated by Gavia. If you need to change this in some
	// way, please refer to indernal/ui/common.go
	ui.BaseData

	OperatingSystems []*operatingsystem.OperatingSystem
	OperatingSystem  *operatingsystem.OperatingSystem // Does it truly need to be a *?
	SearchTerm       string
	// The maximum amount of operatingsystems to show on the operatingsystem-list.html
	// component.
	Limit      int
	EditorMode string
	FormAction string
	FormSubmit string
	NoticeHTML template.HTML
	ErrorHTML  template.HTML
}

func NewHandler(l *slog.Logger, uiFS fs.FS, db *sql.DB) *Handler {
	t := template.Must(template.ParseFS(uiFS,
		"layout/base.html",
		"features/operating_systems/views/*.html",
		"components/*.html",
	))

	return &Handler{
		logger: l,
		tmpl:   t,
		osRepo: operatingsystem.NewOperatingSystemRepository(db),
	}
}

func (h *Handler) Index(w http.ResponseWriter, r *http.Request) {
	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list operating systems", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	ui.WriteHTMLHeader(w)
	if h.isListRequest(r) {
		h.renderTemplate(w, "os-list", data)
		return
	}

	if h.isEditorRequest(r) {
		h.renderTemplate(w, "os-editor-panel", data)
		return
	}

	h.renderTemplate(w, "base", data)

}

// New serves GET /os/new and prepares the editor in create mode.
// The same endpoint supports full-page rendering and editor-only HTMX swaps.
func (h *Handler) New(w http.ResponseWriter, r *http.Request) {
	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list operating systems", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	data.EditorMode = "new"
	data.FormAction = "/os"
	data.FormSubmit = "Create operating system"
	data.OperatingSystem = &operatingsystem.OperatingSystem{}

	ui.WriteHTMLHeader(w)
	if h.isEditorRequest(r) {
		h.renderTemplate(w, "os-editor-panel", data)
		return
	}

	h.renderTemplate(w, "base", data)
}

// Show serves GET /os/{id} and loads the detail panel.
// It keeps list state alive so users can inspect rows without losing current filters.
func (h *Handler) Show(w http.ResponseWriter, r *http.Request) {
	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list operating systems", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	id := r.PathValue("id")
	p, err := h.osRepo.GetByID(r.Context(), id)
	if err != nil {
		h.logger.Error("Failed to load operating system", "id", id, "err", err)
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("bad", "Unable to load operating system.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusBadRequest)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	if p == nil {
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("warn", "Operating system not found.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusNotFound)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "os-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	data.EditorMode = "detail"
	data.OperatingSystem = p

	ui.WriteHTMLHeader(w)
	if h.isEditorRequest(r) {
		h.renderTemplate(w, "os-editor-panel", data)
		return
	}

	h.renderTemplate(w, "base", data)
}

