// Show serves GET /providers/{id} and loads the detail panel.
// It keeps list state alive so users can inspect rows without losing current filters.
func (h *Handler) Show(w http.ResponseWriter, r *http.Request) {
	start := time.Now()
	data, err := h.loadPageData(r, start)
	if err != nil {
		h.logger.Error("Failed to list providers", "err", err)
		http.Error(w, "Not found", http.StatusNotFound)
		return
	}

	id := r.PathValue("id")
	p, err := h.providerRepo.GetByID(r.Context(), id)
	if err != nil {
		h.logger.Error("Failed to load provider", "id", id, "err", err)
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("bad", "Unable to load provider.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusBadRequest)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "provider-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	if p == nil {
		data.EditorMode = "flash"
		data.ErrorHTML = bannerHTML("warn", "Provider not found.")
		ui.WriteHTMLHeader(w)
		w.WriteHeader(http.StatusNotFound)
		if h.isEditorRequest(r) {
			h.renderTemplate(w, "provider-editor-panel", data)
			return
		}
		h.renderTemplate(w, "base", data)
		return
	}

	data.EditorMode = "detail"
	data.Provider = p

	ui.WriteHTMLHeader(w)
	if h.isEditorRequest(r) {
		h.renderTemplate(w, "provider-editor-panel", data)
		return
	}

	h.renderTemplate(w, "base", data)
}

