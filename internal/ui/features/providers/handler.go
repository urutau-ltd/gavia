	id := r.PathValue("id")
	name := strings.TrimSpace(r.Form.Get("name"))
	website := strings.TrimSpace(r.Form.Get("website"))
	notes := strings.TrimSpace(r.Form.Get("notes"))

	data.EditorMode = "edit"
	data.FormAction = fmt.Sprintf("/providers/%s/edit", id)
